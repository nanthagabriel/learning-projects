# COLLEGE ALGEBRA WITH PYTHON
# RATIO AND PROPORTIONS
# n1/d1 = n2/d2

# Use a zero in the place of an unknown value, 
# Current values are set to an Exchange rate source,
# SGD to USD 
n1 = 1
d1 = 1.6
n2 = 0
d2 = 1

if n2 == 0:
    answer = d2 * n1 / d1
    print("n2 = ", answer)

if d2 == 0:
    answer = n2 * d1 / n1
    print("d2 = ", answer)
